    public static Bitmap zoomBitmap(Bitmap bitmap, float scale, int quality) {
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        BitmapFactory.Options opt = new BitmapFactory.Options();
        bitmap.compress(Bitmap.CompressFormat.PNG, quality, baos);
        opt.inScaled = true;
        opt.outWidth = (int) (bitmap.getWidth()* scale);
        opt.outHeight = (int) (bitmap.getHeight() * scale);
        bitmap.recycle();
        bitmap = BitmapFactory.decodeByteArray(baos.toByteArray(), 0, baos.size(), opt);
        Matrix matrix = new Matrix();
        matrix.postScale(opt.outWidth, opt.outHeight);
        Bitmap nbm = Bitmap.createBitmap(bitmap, 0, 0, opt.outWidth, opt.outHeight, matrix, true);
        bitmap.recycle();
        return nbm;
    }

